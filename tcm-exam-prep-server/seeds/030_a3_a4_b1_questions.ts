import { Knex } from 'knex'

interface QR {
  id: string; question_type: string; is_group_root: number; group_id: string | null
  subject_id: string; chapter_id: string; section_id: string
  knowledge_point_ids_json: string; difficulty: number; exam_years_json: string
  question_stem: string; options_json: string; shared_options_json: string | null
  correct_answer: string; explanation: string; tags_json: string; order_in_group: number
}

const opts = (arr: string[]) => JSON.stringify(arr.map((t, i) => ({ key: String.fromCharCode(65 + i), text: t })))
const tag = (arr: string[]) => JSON.stringify(arr)

function q(
  id: string, type: string, grp: number, gid: string | null, subj: string, ch: string,
  diff: number, stem: string, options: string[], ans: string, expl: string, tags: string[], ord = 0
): QR {
  return {
    id, question_type: type, is_group_root: grp, group_id: gid, subject_id: subj, chapter_id: ch,
    section_id: '', knowledge_point_ids_json: '[]', difficulty: diff, exam_years_json: '[]',
    question_stem: stem, options_json: opts(options), shared_options_json: null,
    correct_answer: ans, explanation: expl, tags_json: tag(tags), order_in_group: ord,
  }
}

function root(
  id: string, type: string, subj: string, ch: string,
  stem: string, sharedOpts: string[] | null, tags: string[], diff = 3
): QR {
  return {
    id, question_type: type, is_group_root: 1, group_id: null, subject_id: subj, chapter_id: ch,
    section_id: '', knowledge_point_ids_json: '[]', difficulty: diff, exam_years_json: '[]',
    question_stem: stem, options_json: '[]',
    shared_options_json: sharedOpts ? opts(sharedOpts) : null,
    correct_answer: '', explanation: '', tags_json: tag(tags), order_in_group: 0,
  }
}

function sub(
  id: string, type: string, gid: string, subj: string, ch: string,
  diff: number, stem: string, options: string[], ans: string, expl: string, tags: string[], ord: number
): QR {
  return {
    id, question_type: type, is_group_root: 0, group_id: gid, subject_id: subj, chapter_id: ch,
    section_id: '', knowledge_point_ids_json: '[]', difficulty: diff, exam_years_json: '[]',
    question_stem: stem, options_json: opts(options), shared_options_json: null,
    correct_answer: ans, explanation: expl, tags_json: tag(tags), order_in_group: ord,
  }
}

function b1sub(
  id: string, gid: string, subj: string, ch: string,
  diff: number, stem: string, ans: string, expl: string, tags: string[], ord: number
): QR {
  return {
    id, question_type: 'B1', is_group_root: 0, group_id: gid, subject_id: subj, chapter_id: ch,
    section_id: '', knowledge_point_ids_json: '[]', difficulty: diff, exam_years_json: '[]',
    question_stem: stem, options_json: '[]', shared_options_json: null,
    correct_answer: ans, explanation: expl, tags_json: tag(tags), order_in_group: ord,
  }
}

